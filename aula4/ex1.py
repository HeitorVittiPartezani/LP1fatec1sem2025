# Usando o laço for
print("Contagem de 1 a 10 com for:")
for i in range(1, 11):
    print(i, end=" ")
print("\n") # Pula uma linha para separar as saídas

# Usando o laço while
print("Contagem de 1 a 10 com while:")
contador = 1
while contador <= 10:
    print(contador, end=" ")
    contador += 1
print()